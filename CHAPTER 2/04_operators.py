#Arithmetic operators
a = 10
b = 20
c= a + b
print(c)  # Addition

# Assignment operators
a = 4 - 2
print(a)  
b = 6
b += 3 # Increment b by 3 and then assign it to b
print(b)  # b is now 9

# Comparison operators
d = 5<=4
print(d)
e = 5 != 4
print(e)  # True

# Logical operators
f = True or False
print(f)  # True
g = True and False
print(g)  # False

