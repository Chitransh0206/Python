#For tuples
t = (1, 4, 6, 234, 6, 764)
for i in t:
    print(i)
# This is a tuple, not a list, but the iteration works the same way.
# A tuple is immutable, so you cannot change its elements.

#For strings
# You can also iterate over a string.
s = "Harry"
for character in s:
    print(character)    # This will print each character in the string "Harry".

