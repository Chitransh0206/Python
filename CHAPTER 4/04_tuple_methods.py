a = (1, 45, 352, 3424, False, "Rohan", "Shivam")  # Creating a tuple
print(a)  # Output: (1, 45, 352, 3424, False, 'Rohan', 'Shivam')

no = a.count(45)  # Counting occurrences of an item in the tuple
print(no)  # Output: 1

i = a.index("Rohan")  # Finding the index of an item in the tuple
print(i)  # Output: 5