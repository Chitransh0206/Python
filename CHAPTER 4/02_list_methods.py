friends = ["Apple", "Orange", 5, 345.06, False, "Aakash", "Rohan"]
print(friends)

friends.append("Harry")  # Adding an item to the end of the list
print(friends)

l1 = [1, 2, 5, 3, 4]
l1.sort()  # Sorting the list in ascending order
print(l1)
l1.reverse()  # Reversing the list
print(l1)
l1.insert(2, 222)  # Inserting an item at a specific index
print(l1)
l1.pop(2) # Removing an item at a specific index
print(l1)
l1.remove(5)  # Removing an item by value
print(l1)